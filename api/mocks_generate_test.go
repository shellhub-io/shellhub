// TestMocksV3_WrongSiblingFilesAbsent verifies that incorrectly-named mock files
// that would be generated by a misconfigured v2 run are absent.
func TestMocksV3_WrongSiblingFilesAbsent(t *testing.T) {
	root := repoRoot(t)

	wrongFiles := []string{
		"api/services/mocks/service.go",
		"pkg/hash/mocks/hasher.go",
	}

	for _, rel := range wrongFiles {
		abs := filepath.Join(root, rel)
		_, err := os.Stat(abs)
		assert.True(t, os.IsNotExist(err), "wrong sibling file should not exist: %s", rel)
	}
}

// TestMocksV3_NoBuildTag verifies that no generated mock file contains a
// //go:build constraint (mockery v3 does not emit build tags by default).
func TestMocksV3_NoBuildTag(t *testing.T) {
	root := repoRoot(t)
	buildTagRe := regexp.MustCompile(`^//go:build`)

	for _, rel := range mockFiles() {
		abs := filepath.Join(root, rel)
		lines := readLines(t, abs)

		for _, line := range lines {
			assert.False(t, buildTagRe.MatchString(line),
				"mock file %s must not contain a //go:build tag: %q", rel, line)
		}
	}
}
