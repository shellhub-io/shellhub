package mocks_test

import (
	"go/ast"
	"go/parser"
	"go/token"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

// TestSocketMockV3 verifies that the generated ssh/web/mocks/socket.go was
// produced by mockery v3: correct package name, struct name, v3 header comment,
// and no //go:build constraint.
func TestSocketMockV3(t *testing.T) {
	const mockFile = "socket.go"

	fset := token.NewFileSet()

	f, err := parser.ParseFile(fset, mockFile, nil, parser.ParseComments)
	require.NoError(t, err, "socket.go must be valid Go")

	t.Run("package is mocks", func(t *testing.T) {
		assert.Equal(t, "mocks", f.Name.Name)
	})

	t.Run("v3 header present", func(t *testing.T) {
		// The first comment group in a mockery-v3 file reads:
		//   // Code generated by mockery; DO NOT EDIT.
		//   // github.com/vektra/mockery
		//   // template: testify
		require.NotEmpty(t, f.Comments, "file must have comments")

		firstGroup := f.Comments[0]

		var combined strings.Builder

		for _, c := range firstGroup.List {
			combined.WriteString(c.Text)
			combined.WriteString("\n")
		}

		header := combined.String()
		assert.Contains(t, header, "Code generated by mockery", "v3 generated header missing")
		assert.Contains(t, header, "github.com/vektra/mockery", "mockery source URL missing")
	})

	t.Run("no go:build constraint", func(t *testing.T) {
		for _, cg := range f.Comments {
			for _, c := range cg.List {
				assert.NotContains(t, c.Text, "//go:build", "unexpected //go:build tag found")
			}
		}
	})

	t.Run("struct Socket defined", func(t *testing.T) {
		found := false

		for _, decl := range f.Decls {
			genDecl, ok := decl.(*ast.GenDecl)
			if !ok || genDecl.Tok != token.TYPE {
				continue
			}

			for _, spec := range genDecl.Specs {
				ts, ok := spec.(*ast.TypeSpec)
				if !ok {
					continue
				}

				if ts.Name.Name == "Socket" {
					if _, isStruct := ts.Type.(*ast.StructType); isStruct {
						found = true
					}
				}
			}
		}

		assert.True(t, found, "struct Socket must be defined in socket.go")
	})
}
