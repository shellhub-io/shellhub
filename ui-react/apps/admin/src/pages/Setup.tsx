import { useState, useEffect, FormEvent } from "react";
import { useNavigate, useSearchParams } from "react-router-dom";
import {
  CheckIcon,
  DocumentDuplicateIcon,
  ExclamationCircleIcon,
  EyeSlashIcon,
  EyeIcon,
} from "@heroicons/react/24/outline";
import { setup } from "../api/system";
import { validate, type FormErrors } from "./setup/validate";

export default function Setup() {
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();

  const signFromQuery = searchParams.get("sign") || "";

  const [step, setStep] = useState(signFromQuery ? 2 : 1);
  const [sign, setSign] = useState(signFromQuery);
  const [name, setName] = useState("");
  const [username, setUsername] = useState("");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [confirmPassword, setConfirmPassword] = useState("");
  const [showPassword, setShowPassword] = useState(false);
  const [showConfirm, setShowConfirm] = useState(false);
  const [touched, setTouched] = useState<Record<string, boolean>>({});
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");
  const [success, setSuccess] = useState(false);
  const [copied, setCopied] = useState(false);

  const errors = validate({ name, username, email, password, confirmPassword });

  useEffect(() => {
    if (success) {
      const timer = setTimeout(
        () => navigate("/login", { replace: true }),
        3000,
      );
      return () => clearTimeout(timer);
    }
  }, [success, navigate]);

  const handleBlur = (field: string) => {
    setTouched((prev) => ({ ...prev, [field]: true }));
  };

  const showError = (field: keyof FormErrors) =>
    touched[field] ? errors[field] : undefined;

  const handleCopy = () => {
    navigator.clipboard.writeText("./bin/setup");
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleNextStep = (e: FormEvent) => {
    e.preventDefault();
    if (sign.trim()) {
      setStep(2);
    }
  };

  const handleSubmit = async (e: FormEvent) => {
    e.preventDefault();

    setTouched({
      name: true,
      username: true,
      email: true,
      password: true,
      confirmPassword: true,
    });

    if (Object.keys(errors).length > 0) return;

    setLoading(true);
    setError("");

    try {
      await setup(sign, { name, username, email, password });
      setSuccess(true);
    } catch (err: unknown) {
      if (
        err &&
        typeof err === "object" &&
        "response" in err &&
        (err as { response?: { status?: number } }).response?.status === 409
      ) {
        setError("Setup has already been completed.");
      } else {
        setError(
          "An error occurred. Please check if the signature matches the one generated by the command and try again.",
        );
      }
    } finally {
      setLoading(false);
    }
  };

  if (success) {
    return (
      <div className="w-full max-w-sm mx-auto animate-fade-in">
        <div className="bg-surface border border-border rounded-lg overflow-hidden">
          <div className="px-8 pt-8 pb-6 border-b border-border bg-card/50">
            <div className="flex justify-center mb-5">
              <img src="/v2/ui/logo.svg" alt="ShellHub" className="h-7" />
            </div>
            <p className="text-center text-2xs font-mono text-text-muted tracking-wider uppercase">
              Initial Setup
            </p>
          </div>

          <div className="p-8 text-center">
            <div className="inline-flex items-center justify-center w-12 h-12 rounded-full bg-accent-green/10 mb-4">
              <CheckIcon
                className="w-6 h-6 text-accent-green"
                strokeWidth={2}
              />
            </div>
            <h3 className="text-sm font-semibold text-text-primary mb-2">
              Account created successfully
            </h3>
            <p className="text-xs text-text-secondary leading-relaxed">
              Redirecting to login in a few seconds...
            </p>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="w-full max-w-sm mx-auto animate-fade-in">
      <div className="bg-surface border border-border rounded-lg overflow-hidden">
        <div className="px-8 pt-8 pb-6 border-b border-border bg-card/50">
          <div className="flex justify-center mb-5">
            <img src="/v2/ui/logo.svg" alt="ShellHub" className="h-7" />
          </div>
          <h1 className="text-center text-sm font-semibold text-text-primary mb-1">
            Welcome to ShellHub
          </h1>
          <p className="text-center text-2xs font-mono text-text-muted tracking-wider uppercase">
            Initial Setup
          </p>

          <div className="flex items-center justify-center gap-2 mt-4">
            <StepDot active={step === 1} label="1" />
            <div className="w-6 h-px bg-border" />
            <StepDot active={step === 2} label="2" />
          </div>
        </div>

        <div className="p-8">
          {error && (
            <div className="flex items-start gap-2 bg-accent-red/8 border border-accent-red/20 text-accent-red px-3.5 py-2.5 rounded-md text-xs font-mono animate-slide-down mb-5">
              <ExclamationCircleIcon
                className="w-3.5 h-3.5 shrink-0 mt-0.5"
                strokeWidth={2}
              />
              {error}
            </div>
          )}

          {step === 1 ? (
            <form onSubmit={handleNextStep} className="space-y-5">
              <p className="text-xs text-text-secondary leading-relaxed">
                Run the following command in your terminal to generate a setup
                signature, then paste it below to proceed.
              </p>

              <div>
                <label className="block text-2xs font-mono font-semibold uppercase tracking-label text-text-muted mb-2">
                  Setup Command
                </label>
                <div className="flex items-center bg-card border border-border rounded-md overflow-hidden">
                  <span className="pl-3.5 text-text-muted font-mono text-xs select-none">
                    $
                  </span>
                  <code className="flex-1 text-xs font-mono text-primary px-2 py-2.5 select-all">
                    ./bin/setup
                  </code>
                  <button
                    type="button"
                    onClick={handleCopy}
                    className="px-3 py-2.5 text-text-muted hover:text-text-secondary border-l border-border hover:bg-border/30 transition-colors"
                    title="Copy command"
                  >
                    {copied ? (
                      <CheckIcon
                        className="w-4 h-4 text-accent-green"
                        strokeWidth={2}
                      />
                    ) : (
                      <DocumentDuplicateIcon className="w-4 h-4" />
                    )}
                  </button>
                </div>
              </div>

              <div>
                <label
                  htmlFor="sign"
                  className="block text-2xs font-mono font-semibold uppercase tracking-label text-text-muted mb-2"
                >
                  Signature
                </label>
                <input
                  id="sign"
                  type="text"
                  value={sign}
                  onChange={(e) => setSign(e.target.value)}
                  required
                  autoFocus
                  className="w-full px-3.5 py-2.5 bg-card border border-border rounded-md text-sm text-text-primary font-mono placeholder:text-text-secondary focus:outline-none focus:border-primary/50 focus:ring-1 focus:ring-primary/20 transition-all duration-200"
                  placeholder="Paste signature here"
                />
              </div>

              <button
                type="submit"
                className="w-full bg-primary hover:bg-primary-600 text-white py-2.5 px-4 rounded-md text-sm font-semibold transition-all duration-200 mt-1"
              >
                Next
              </button>
            </form>
          ) : (
            <form onSubmit={handleSubmit} className="space-y-4">
              <p className="text-xs text-text-secondary leading-relaxed mb-1">
                Set up your admin account with your personal information.
              </p>

              <InputField
                id="name"
                label="Name"
                value={name}
                onChange={setName}
                onBlur={() => handleBlur("name")}
                error={showError("name")}
                placeholder="Your name"
                autoFocus
              />

              <InputField
                id="username"
                label="Username"
                value={username}
                onChange={setUsername}
                onBlur={() => handleBlur("username")}
                error={showError("username")}
                placeholder="username"
              />

              <InputField
                id="email"
                label="Email"
                type="email"
                value={email}
                onChange={setEmail}
                onBlur={() => handleBlur("email")}
                error={showError("email")}
                placeholder="you@example.com"
              />

              <PasswordField
                id="password"
                label="Password"
                value={password}
                onChange={setPassword}
                onBlur={() => handleBlur("password")}
                error={showError("password")}
                placeholder="Min. 5 characters"
                visible={showPassword}
                onToggle={() => setShowPassword((v) => !v)}
              />

              <PasswordField
                id="confirmPassword"
                label="Confirm Password"
                value={confirmPassword}
                onChange={setConfirmPassword}
                onBlur={() => handleBlur("confirmPassword")}
                error={showError("confirmPassword")}
                placeholder="Re-enter password"
                visible={showConfirm}
                onToggle={() => setShowConfirm((v) => !v)}
              />

              <div className="flex gap-3 pt-1">
                <button
                  type="button"
                  onClick={() => setStep(1)}
                  className="flex-1 bg-card hover:bg-border/50 border border-border text-text-secondary py-2.5 px-4 rounded-md text-sm font-semibold transition-all duration-200"
                >
                  Back
                </button>
                <button
                  type="submit"
                  disabled={loading}
                  className="flex-[2] bg-primary hover:bg-primary-600 text-white py-2.5 px-4 rounded-md text-sm font-semibold disabled:opacity-dim disabled:cursor-not-allowed transition-all duration-200"
                >
                  {loading ? (
                    <span className="flex items-center justify-center gap-2">
                      <span className="w-3.5 h-3.5 border-2 border-white/30 border-t-white rounded-full animate-spin" />
                      <span className="font-mono text-xs">Creating...</span>
                    </span>
                  ) : (
                    "Create Account"
                  )}
                </button>
              </div>
            </form>
          )}
        </div>
      </div>

      <p className="text-center text-2xs font-mono text-text-muted/40 mt-6">
        ShellHub &mdash; Secure Remote Access
      </p>
    </div>
  );
}

function StepDot({ active, label }: { active: boolean; label: string }) {
  return (
    <div
      className={`w-5 h-5 rounded-full flex items-center justify-center text-3xs font-mono font-bold transition-colors ${
        active ? "bg-primary text-white" : "bg-border text-text-muted"
      }`}
    >
      {label}
    </div>
  );
}

function InputField({
  id,
  label,
  type = "text",
  value,
  onChange,
  onBlur,
  error,
  placeholder,
  autoFocus,
}: {
  id: string;
  label: string;
  type?: string;
  value: string;
  onChange: (v: string) => void;
  onBlur: () => void;
  error?: string;
  placeholder: string;
  autoFocus?: boolean;
}) {
  return (
    <div>
      <label
        htmlFor={id}
        className="block text-2xs font-mono font-semibold uppercase tracking-label text-text-muted mb-2"
      >
        {label}
      </label>
      <input
        id={id}
        type={type}
        value={value}
        onChange={(e) => onChange(e.target.value)}
        onBlur={onBlur}
        autoFocus={autoFocus}
        className={`w-full px-3.5 py-2.5 bg-card border rounded-md text-sm text-text-primary font-mono placeholder:text-text-secondary focus:outline-none focus:border-primary/50 focus:ring-1 focus:ring-primary/20 transition-all duration-200 ${
          error ? "border-accent-red/50" : "border-border"
        }`}
        placeholder={placeholder}
      />
      {error && (
        <p className="text-2xs font-mono text-accent-red mt-1.5">{error}</p>
      )}
    </div>
  );
}

function PasswordField({
  id,
  label,
  value,
  onChange,
  onBlur,
  error,
  placeholder,
  visible,
  onToggle,
}: {
  id: string;
  label: string;
  value: string;
  onChange: (v: string) => void;
  onBlur: () => void;
  error?: string;
  placeholder: string;
  visible: boolean;
  onToggle: () => void;
}) {
  return (
    <div>
      <label
        htmlFor={id}
        className="block text-2xs font-mono font-semibold uppercase tracking-label text-text-muted mb-2"
      >
        {label}
      </label>
      <div className="relative">
        <input
          id={id}
          type={visible ? "text" : "password"}
          value={value}
          onChange={(e) => onChange(e.target.value)}
          onBlur={onBlur}
          className={`w-full px-3.5 py-2.5 pr-10 bg-card border rounded-md text-sm text-text-primary font-mono placeholder:text-text-secondary focus:outline-none focus:border-primary/50 focus:ring-1 focus:ring-primary/20 transition-all duration-200 ${
            error ? "border-accent-red/50" : "border-border"
          }`}
          placeholder={placeholder}
        />
        <button
          type="button"
          onClick={onToggle}
          className="absolute right-2.5 top-1/2 -translate-y-1/2 text-text-muted hover:text-text-secondary transition-colors"
          tabIndex={-1}
        >
          {visible ? (
            <EyeSlashIcon className="w-4 h-4" />
          ) : (
            <EyeIcon className="w-4 h-4" />
          )}
        </button>
      </div>
      {error && (
        <p className="text-2xs font-mono text-accent-red mt-1.5">{error}</p>
      )}
    </div>
  );
}
