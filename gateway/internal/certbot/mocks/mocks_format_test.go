package mocks_test

import (
	"os"
	"path/filepath"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

// mockFiles lists all mock files that must be validated for v3 format compliance.
var mockFiles = []struct {
	filename   string
	structName string
}{
	{"executor.go", "Executor"},
	{"ticker.go", "Ticker"},
}

// TestMocks_v3Header verifies that each generated mock file carries the v3
// mockery header and does NOT carry a //go:build tag (a v2 artefact).
func TestMocks_v3Header(t *testing.T) {
	dir, err := os.Getwd()
	require.NoError(t, err)

	for _, m := range mockFiles {
		t.Run(m.filename, func(t *testing.T) {
			path := filepath.Join(dir, m.filename)
			data, err := os.ReadFile(path)
			require.NoError(t, err, "mock file must exist: %s", path)

			content := string(data)

			// v3 header must be present
			assert.Contains(t, content, "// Code generated by mockery; DO NOT EDIT.",
				"mock file must have v3 generated header")
			assert.Contains(t, content, "// github.com/vektra/mockery",
				"mock file must reference vektra/mockery")

			// v2 artefact must be absent
			assert.NotContains(t, content, "//go:build",
				"mock file must not contain a //go:build tag (v2 artefact)")

			// package must be mocks (not the source package)
			assert.Contains(t, content, "\npackage mocks\n",
				"mock file must declare package mocks")
		})
	}
}

// TestMocks_structNames verifies that the generated struct names have no Mock
// prefix — the v3 convention uses the bare interface name.
func TestMocks_structNames(t *testing.T) {
	dir, err := os.Getwd()
	require.NoError(t, err)

	for _, m := range mockFiles {
		t.Run(m.filename, func(t *testing.T) {
			path := filepath.Join(dir, m.filename)
			data, err := os.ReadFile(path)
			require.NoError(t, err)

			content := string(data)

			// Bare struct declaration must be present (e.g. "type Executor struct {")
			wantDecl := "type " + m.structName + " struct {"
			assert.Contains(t, content, wantDecl,
				"mock struct must use bare interface name (no Mock prefix)")

			// Old v2 prefixed declaration must be absent
			badDecl := "type Mock" + m.structName + " struct {"
			assert.NotContains(t, content, badDecl,
				"mock struct must not use Mock-prefixed name (v2 convention)")

			// NewXxx constructor must use bare name
			wantCtor := "func New" + m.structName + "("
			assert.Contains(t, content, wantCtor,
				"constructor must use bare interface name")

			// Expecter struct must use bare name
			wantExpecterDecl := "type " + m.structName + "_Expecter struct {"
			assert.Contains(t, content, wantExpecterDecl,
				"Expecter struct must use bare interface name")
		})
	}
}
